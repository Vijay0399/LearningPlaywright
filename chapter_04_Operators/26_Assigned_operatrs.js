let a = 10, b = 3;
console.log(a);
console.log(b);

// Assignment operator
let sum = a + b;
console.log(sum);
sum += 5;  // sum = sum + 5
console.log(sum);

let diff = a - b;
console.log(diff);
diff -= 2;  // diff = diff - 2
console.log(diff);

let product = a * b;
console.log(product);
product *= 2;  // product = product * 2
console.log(product);

let quotient = a / b;
console.log(quotient);
quotient /= 2;  // quotient = quotient / 2
console.log(quotient);

console.log("Modulus Operatoper" + a % b);
